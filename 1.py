s = input('Дана строка:')
print('Третий символ:', s[2])
print('Предпоследний символ:', s[-2])
print('Первые пять символов:', s[:5])
print('Вся строка, кроме последних двух символов:', s[:-2])
print('Все символы с четными индексами:', s[::2])
print('Cимволы с нечетными индексами:', s[1::2])
print('Символы в обратном порядке:', s[::-1])
print('Символы строки через один в обратном порядке, начиная с последнего:', s[::-2])
print('Длина данной строки:', len(s))